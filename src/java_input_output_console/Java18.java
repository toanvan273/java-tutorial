package java_input_output_console;

public class Java18 {
    public static void main(String[] args) {
        int a = 100;
        int b = 3;
        double res = 99992229999.258253;
        System.out.println(res);
        System.out.printf("KQ = %,10.2f\n",res);
        System.out.printf("KQ = %-,10.2f\n",res);
        boolean bb = false;
        System.out.printf("%b",bb);
        /*
        int : %d
        char: %c
        String: %s
        double: %f
        boolean: %b
        %: %
         */
    }
}
